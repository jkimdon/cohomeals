<h2>Help: Viewing and changing user info</h2>

<h4><i>Viewing user info</i></h4>

<p>Click on "User info" in the "Go to" line of the trailer of a page. You should be brought to the "Account" tab, which displays your name, birthdate, email address, billing group, and food preferences.</p>

<h4><i>Changing your password</i></h4>

<p>In the right column of the "Account" tab, you can change your password by entering a new password twice and clicking "Set Password".</p>

<h4><i>Changing other user info</i></h4>

<p>Contact the meal coordinator to change information such as food preferences, billing group, and email address.</p>

<h4><i>Managing buddies</i></h4>

<p>Click on the "Buddies" tab at the top of the user info page.</p>

<p>The buddy system is one-way. That is, you can only choose who can sign you up for meals. If you want to sign someone else up for meals, they must go into their account and choose you as a buddy.</p>

<p><i>To allow someone to sign you up for dining and work shifts</i>: Select their name in the drop box and click "Add buddy".</p>

<p><i>To remove the privilege from someone</i>: Click the "Remove" button next to the name of the person you no longer wish to allow to sign you up for dining and work shifts.</p>

