<p><i>Add a user</i>: Click on "User info" in the "Go to" section of the trailer on any page. This should bring you to the "Users" tab. Click "Add New User" at the top of the tab. Enter information and click "Save". </p>

<p><i>Edit a user</i>: Click on "User info" in the "Go to" section of the trailer on any page. This should bring you to the "Users" tab. Click the name of the user you wish to edit. Their information should pop up in the same page. Change the information and click "Save".</p>

