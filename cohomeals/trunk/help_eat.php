<h2>Help: Signing up or cenceling dining</h2>

<h4><i>Sign you or a buddy up for a single meal</i></h4>

<p>From the calendar view (Click on "Calendar" in the "Go to" line on the trailer of a page to get to this view), click on the desired meal.</p>

<p><i>To add yourself</i>: Choose "On-site" or "Take-home" and click "Add me". Your name should appear.</p>

<p><i>To add a buddy (including yourself)</i>: Choose "On-site" or "Take-home" and click "Add buddy". A popup window will appear with all your buddies who have not yet signed up to eat. Check the names of everyone you wish to sign up, then click "Submit". The popup box should disappear, and the names should appear on the meal detail page.</p>

<p><i>To cancel dining</i>: Click the "Remove" button by the name of the person you wish to cancel.</p>

<p><i>To change between on-site dining and take-home plate</i>: Click the corresponding "Change to ..." button next to the name of the person you wish to cancel</p>

<p></p>

<h4><i>Sign you or a buddy up for a set of club meals</i></h4>

<p>Click on "Club" under "Manage subscriptions" in the trailer of a page to get to the club management page. All the available clubs will be listed, including their date/times and meal notes to help you identify the clubs.</p>

<p><i>To subscribe yourself</i>: Locate the desired club and click "Subscribe me". The link should change to "Unsubscribe me" to indicate that you are subscribed.</p

<p><i>To subscribe a buddy</i>: Click "Subscribe buddy". A popup window will appear with all your buddies who are not yet subscribed. Check the names of those you wish to subscribe and click "Submit".</p>

<p><i>To cancel a buddy</i>: Click "Unsubscribe buddy". A popup window will appear with all your buddies who are subscribed. Check the names of those you wish to cancel and click "Submit".</p>

<p></p>

<h4><i>Sign you or a buddy up for a set of heart meals</i></h4>

<p>Click on "Heart" under "Manage subscriptions" in the trailer of a page to get to the heart management page. The first line on the heart management page tells you which person you are signing up or cancelling. The select box includes you and your buddies. The default is you. Be sure to change this if you wish to sign up a buddy.</p>

<p>The current status of the person you selected is shown on the next line.</p>

<p>If they are not yet signed up, there will be an option to sign up on either an ongoing basis or for a limited time. "Ongoing" means that you are signed up for all meals or all meals on certain days of the week until you say otherwise. "Liminted time" means that you sign up for a minimum of three months, and you individually select which days you sign up for.</p>

<p><i>Ongoing basis</i>: You may choose to attend all meals or skip one of the three days per week. Then select the starting date either by choosing the day with the drop boxes or by clicking "Select..." which pops up a calendar on which you click on your chosen day. Finally, click "Submit".</p>

<p><i>Limited time</i>: Select your starting and ending days (either with the drop boxes or by using the popup calendar by clicking "Select..."). The starting and ending days must be at least three months apart. Then click "Submit". A popup window will appear showing a calendar with checkboxes on each day. Days where heart meals are scheduled are clickable. Check the days you wish to sign up for, then click "Submit" at the bottom. Note that to subscribe with the volume discount, you must choose at least 2/3 of the available heart meals. If you wish to sign up for fewer than 2/3, sign up individually for each meal via the instructions at the beginning of this help window.</p>




